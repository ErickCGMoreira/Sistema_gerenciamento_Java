    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        nome.setText(funcionario.getNome());
        email.setText(funcionario.getEmail());
        senha.setText(funcionario.getSenha());
        cpf.setText(funcionario.getCpf());
        cep.setText(funcionario.getCep());
        endereco.setText(funcionario.getEndereco());
        contato.setText(funcionario.getContato()+"");
        
        funcionario.setCep(cep.getText());
        funcionario.setContato(contato.getText());
        funcionario.setEndereco(endereco.getText());
        funcionario.setCpf(cpf.getText());
        funcionario.setNome(nome.getText());
        funcionario.setEmail(email.getText());
        funcionario.setSenha(senha.getText());
        funcionario.setPremium(pSim.isSelected());
        try {
            if(add){
                ClienteDAO.salvar(funcionario);
            }else{
            ClienteDAO.alterar(funcionario);
            }
            JOptionPane.showMessageDialog(rootPane, "Sucesso!");
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(AlterarCliente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Erro! Verifique os dados e tente novamente");
        }
        
    }//GEN-LAST:event_salvarActionPerformed


    public AlterarCliente(Cliente funcionario, boolean add) {
        this.funcionario = funcionario;
        this.add = add;
        initComponents();
        buttonGroup1.add(pSim);
        buttonGroup1.add(pNao);
        if(!add){
            nome.setText(funcionario.getNome());
            email.setText(funcionario.getEmail());
            senha.setText(funcionario.getSenha());
            cpf.setText(funcionario.getCpf());
            cep.setText(funcionario.getCep());
            endereco.setText(funcionario.getEndereco());
            contato.setText(funcionario.getContato()+"");
            if(funcionario.isPremium()){
                pSim.setSelected(true);
                pNao.setSelected(false);
            }else{
                pSim.setSelected(false);
                pNao.setSelected(true);
            }
        }
    }

